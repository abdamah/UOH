# Python Indentation and Block Code
# ---------------------------------
# Python uses indentation (spaces or tabs) to define the structure of the code.
# It does not use braces `{}` like other languages.
# A group of indented lines forms a block of code.

# Example of indentation:
if True:
    print("This is inside the block.")  # Indented by 4 spaces
print("This is outside the block.")     # Not indented, outside the block
