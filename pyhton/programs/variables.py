name = "Abdallah"  # string data type
age = 25  # integer data type
_ = 99  # variable name can start with underscore
fee = 25.99  # float data type
course_name = "Python"  # string data type
course_duration = 3  # integer data type
is_admin = True  # boolean data type
complex_number = 3 + 4j     # complex data type


# Print the variables
print("Name:", name)
print("Age:", age)
print("Fee:", fee)
print("Course Name:", course_name)
print("Course Duration:", course_duration)
print("Is Admin:", is_admin)
print("Complex Number:", complex_number)

# Print the type of the variables
print("Type of name:", type(name))
print("Type of age:", type(age))
print("Type of fee:", type(fee))
print("Type of course_name:", type(course_name))
print("Type of course_duration:", type(course_duration))
print("Type of is_admin:", type(is_admin))
print("Type of complex_number:", type(complex_number))

# new variable
account = '123456789'
print("Account:", account)
print("Type of account:", type(account))

account = 123456789
print("Account:", account)
print("Type of account:", type(account))

# Make sure that type of variable will be referred from latest assignment value
# So be alert when you are using the same variable name


